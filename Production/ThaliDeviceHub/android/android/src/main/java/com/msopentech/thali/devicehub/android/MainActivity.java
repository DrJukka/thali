package com.msopentech.thali.devicehub.android;

import android.app.Activity;
import android.content.Intent;
import android.os.Bundle;
import android.view.Menu;
import android.view.MenuItem;
import android.webkit.WebView;
import com.msopentech.thali.devicehub.universal.ThaliDeviceHubUx;

public class MainActivity extends Activity {

    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        setContentView(R.layout.activity_main);

        Intent intent = new Intent(this, ThaliDeviceHubService.class);
        startService(intent);

        WebView webView = (WebView) findViewById(R.id.webview);
        String rootHtmlFileAsString = ThaliDeviceHubUx.getRootUxHtmlAsString();
        webView.loadData(rootHtmlFileAsString, "text/html", "utf-8");
    }

    @Override
    public boolean onCreateOptionsMenu(Menu menu) {
        
        // Inflate the menu; this adds items to the action bar if it is present.
        getMenuInflater().inflate(R.menu.main, menu);
        return true;
    }

    @Override
    public boolean onOptionsItemSelected(MenuItem item) {
        // Handle action bar item clicks here. The action bar will
        // automatically handle clicks on the Home/Up button, so long
        // as you specify a parent activity in AndroidManifest.xml.
        switch (item.getItemId()) {
            case R.id.action_settings:
                return true;
        }
        return super.onOptionsItemSelected(item);
    }

    // TODO: Hook into the destructor so we can close down the service, it should happen automatically but lets be tidy
}
